# Python Syntax & Indentation

x = 10
if x > 5:
    print("x is greater than 5")  # This line is indented → part of the if-block
    print("Indentation is very important in Python!")  # Still inside if-block

print("This line is outside the if-block")  # Not indented → outside if-block


# Print Statements

# Printing strings
print("Hello, Python!")

# Printing numbers
print(2025)

# Printing multiple values
name = "Rakib"
age = 18
print("My name is", name, "and I am", age, "years old.")

# f-string (formatted string literal)
print(f"My name is {name} and I am {age} years old.")


